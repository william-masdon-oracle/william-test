  CREATE OR REPLACE EDITIONABLE TRIGGER "LL_LIVESTACKS_AIU_TRG" AFTER 
    INSERT OR UPDATE on ll_livestacks
    FOR EACH ROW 
begin

-- update the livestack tag
insert into ll_livestack_tags(tag_id, livestack_id)
select 48, :new.id from dual where not exists (select * from ll_livestack_tags where tag_id = 48 and livestack_id = :new.id);


-- update workshop vectors 
if inserting or :old.name != :new.name or :old.desc_short != :new.desc_short or :old.desc_long != :new.desc_long then 
    ll_pkg_ai.VECTORIZE_LIVESTACK(
        p_livestack_id => :new.id,
        p_name        => :new.name,
        p_desc_short  => :new.desc_short, 
        p_desc_long   => :new.desc_long
    );
end if;
END;
